def parse_trypos_sybyl(mol2file):

    with open(mol2file) as molfile:
        read = False
        sybyldict = {}
        for line in molfile.readlines():
            if line.startswith('@<TRIPOS>ATOM'):
                read = True
                continue
            if line.startswith('@<TRIPOS>BOND'):
                break
            if read:
                line = line.split()
                atoms = line[1]
                sybyl = line[5]
                resid = line[7][0:3]
                if resid.isalpha():
                    sybyldict['{0}-{1}'.format(resid, atoms)] = sybyl

    return sybyldict
